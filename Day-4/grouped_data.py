states_of_america = ["Texas", "Florida", "Maine"] #Lists are ordered

print(states_of_america[0]) #Prints Texas

print(states_of_america[-1]) #Prints last item in list

states_of_america[0] = "Bexas" #Changes item in list

print(states_of_america[0])

states_of_america.append("Adamland") #Adds item to end of list

print(states_of_america)

states_of_america.extend(["Poopland", "Peepland"]) #Adds more than one item to end of list

print(states_of_america)